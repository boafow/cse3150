// Basic functions for numbers: 
// (i) ECSquareN: take an integer and return its square
// (ii) ECDoubleN: take an integer and return two times of it

// Your code here...
#ifndef __EC_NUMBERS_H
#define __EC_NUMBERS_H
namespace ECNumbers {
	
	int ECSquareN(int n){
		return n*n;
	};

	int ECDoubleN(int n){
		return 2*n;
	};

};
#endif
